// Assigning text to variables

var message;

message = "Hello World!";
console.log(message);

message = 'Congratulations! Your tweet has won a prize...';
console.log(message);

message = "Thank You!";
console.log(message);

message = "Hello " + "World!";
console.log(message)
message = 'Congratulations!' + ' Your' + ' tweet has won a prize...';
console.log(message);



